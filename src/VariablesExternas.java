public class VariablesExternas {
    private boolean agua = false;
    private boolean CH4 = false;
    private boolean CO = false;

    public VariablesExternas() {}

    public void setAgua(boolean agua) {
        this.agua = agua;
    }

    public void setCH4(boolean CH4) {
        this.CH4 = CH4;
    }

    public void setCO(boolean CO) {
        this.CO = CO;
    }

    public boolean getAgua() {
        return agua;
    }

    public boolean getCH4() {
        return CH4;
    }

    public boolean getCO() {
        return CO;
    }
}
